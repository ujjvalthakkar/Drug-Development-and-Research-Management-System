    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) studyRequestsJTable.getModel();
        model.setRowCount(0);
       
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
           
            Object[] row = new Object[6];
            row[0] = ((StudyWorkRequest) request).getStudy().getStudyName();
            row[1] = request;
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = request.getStatus();
            String result = ((StudyWorkRequest) request).getDrug() == null ? "Yet to be Decided" : ((StudyWorkRequest) request).getDrug().getDrugName();
            row[4] = result;
            row[5] = ((StudyWorkRequest) request).getStudy().getRecruitmentStatus();
            model.addRow(row);
        }
    }

