    private void assignPatientsJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignPatientsJBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = studyRequestsJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row!");
            return;
        } else if (((StudyWorkRequest) studyRequestsJTable.getValueAt(selectedRow, 1)).getStudy().getRecruitmentStatus().equals("New")) {
            JOptionPane.showMessageDialog(this, "Patient not yet requested!!");
            return;
        } else if (((StudyWorkRequest) studyRequestsJTable.getValueAt(selectedRow, 1)).getStudy().getRecruitmentStatus().equals("Patients Assigned")) {
            JOptionPane.showMessageDialog(this, "Patient already Assigned!!");
            return;
        } else {
            StudyWorkRequest req = (StudyWorkRequest) studyRequestsJTable.getValueAt(selectedRow, 1);
            int requiredCount = req.getStudy().getRecruitmentNumber();
            int currentCount = 0;
            for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof PatientOrganization) {
                    for (Patient p : o.getPatientDirectory().getPatientDirectory()) {
                        currentCount++;
                        if (currentCount<=requiredCount) {
                            req.getStudy().addPatient(p); // Adding required number of patients to the study patient list
                        }
                        else{
                            break;
                        }
                    }
                    req.getStudy().setRecruitmentStatus("Patients Assigned");
                }
            }
            JOptionPane.showMessageDialog(this, "Patient assigned successfully!");
            populateTable();
        }
    }//GEN-LAST:event_assignPatientsJBtnActionPerformed

