    public void populateCompoundTable() {
        DefaultTableModel model = (DefaultTableModel) compoundJTable.getModel();
        model.setRowCount(0);

        for (Compound c : tempList) {
            Object row[] = new Object[3];
            row[0] = c;
            row[1] = c.getMolecularWeight();
            row[2] = c.getQuantity();
            model.addRow(row);
        }

    }


