    private void createJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJBtnActionPerformed
        // TODO add your handling code here:
        String drugName = drugNameJTextField.getText();
        String drugDesciprtion = descriptionJTextArea.getText();
        String drugUses = usesJTextField.getText();

        if (drugName.equals("") || drugDesciprtion.equals("") || drugUses.equals("")) {
            JOptionPane.showMessageDialog(this, "Please provide the Drug name,description and its uses!!");
            return;
        } else if (organization instanceof ScientistOrganization) {
            for (Scientist s : organization.getScientistDirectory().getScientistDirectory()) {
                if (account.getEmployee().getName().equals(s.getName())) {
                    Drug d = s.addDrugInList();
                    d.setDrugName(drugNameJTextField.getText());
                    d.setDescription(descriptionJTextArea.getText());
                    d.setUses(usesJTextField.getText());
                    d.setCopoundComposition(tempList);
                    d.setClinicalTrialStatus("Pending");
                    JOptionPane.showMessageDialog(this, "Drug Created Successfully!!");
                }
            }
        }

    }//GEN-LAST:event_createJBtnActionPerformed

    private void backJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJbtnActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        if (component instanceof ScientistWorkAreaJPanel) {
            ScientistWorkAreaJPanel s = (ScientistWorkAreaJPanel) component;
            s.populateDrugTable();
        }
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJbtnActionPerformed


